const avatarRow = (target, data) => {
      const targetElm = document.getElementById(target);

      for(const key in data) {
        const row = document.createElement("div");
        row.classList.add("avatar-module-row", "staff_list", "fadeUpTrigger");
        let data_row = data[key];

        for(const k in data_row) {
            let data_item = data_row[k];
            const fig = document.createElement("figure");
            const figavatar = document.createElement("div");
            figavatar.classList.add("avatar-module-image");
            const avatarimg = document.createElement("img");
            avatarimg.setAttribute("src", "./avatar-module/" + data_item.id + ".webp");
            avatarimg.setAttribute("alt", data_item.name);
            figavatar.appendChild(avatarimg);
            fig.appendChild(figavatar);
            if(!("Empty" === data_item.name)) {
                const figcap = document.createElement("figcaption");
                figcap.textContent = data_item.name;
                fig.appendChild(figcap);
            }
            row.appendChild(fig);
        }
        targetElm.appendChild(row);
    }
}